#!/usr/bin/env python3
"""
generate_mechanism_decks.py

Generates 6 PFLOTRAN input decks isolating the two multiphase controls
that are independent degrees of freedom in the flow model:

  Case C — PHASE PARTITIONING (4 new sims)
      Vary the injection CO2 mole fraction across the dissolved-to-scCO2
      continuum to test whether carbonation tracks the phase split.
      Values: 0.20, 0.40, 0.60, 0.80   (S1=0.04 and S2=0.99 already exist)

  Case D — BUOYANCY OVERRIDE (2 new sims)
      Vary the injection well vertical position (top vs bottom of formation)
      while keeping flow and chemistry fixed. If buoyancy override matters,
      top-injection should outperform bottom-injection.
      Positions: top (z=0-30m), bottom (z=70-100m)
                 (middle z=20-80m exists as the baseline)

  (A gas-residual-saturation sweep was considered as a test of the
   gas-water contact mechanism but was dropped: a uniform change in
   residual saturation alters the trapped-gas QUANTITY, not the gas-water
   contact MORPHOLOGY, and so does not isolate contact area. Gas-water
   contact is addressed structurally in the manuscript via the
   local-equilibrium dissolution argument.)

All cases use the S2 (scCO2) base configuration where the mechanism
matters most, with the single exception of Case C which is the full
mole-fraction continuum and includes both endmembers (S1 and S2).

Usage:
    python3 generate_mechanism_decks.py
Outputs:
    ./decks/caseC_phase_xfrac<value>.in   (Case C)
    ./decks/caseD_buoyancy_<position>.in  (Case D)
"""

