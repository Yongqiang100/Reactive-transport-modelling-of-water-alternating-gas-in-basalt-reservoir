def main():
    out = Path('decks')
    out.mkdir(exist_ok=True)

    # ===================================================================
    # Case C — PHASE PARTITIONING (vary mole fraction)
    # ===================================================================
    # We use a mixed-injection setup (both liquid and gas rate) that lets
    # PFLOTRAN's phase equilibrium determine the actual phase split based
    # on the imposed mole fraction. Total volumetric injection is held
    # constant at 5e-6 m^3/s by partitioning between gas and liquid rates.
    print("=== Case C: PHASE PARTITIONING (vary mole fraction) ===")
    case_C_mole_fractions = [0.20, 0.40, 0.60, 0.80]
    # For each mole fraction, use a co-injection RATE of liquid + gas
    # corresponding to that phase split. Total volumetric: 5e-6 m^3/s
    for x in case_C_mole_fractions:
        # Proportion of injection that arrives as gas vs liquid
        # (use the mole fraction as a rough proxy; actual phase
        # partitioning is determined by PFLOTRAN's EOS)
        rate_g = 5.0e-6 * x
        rate_l = 5.0e-6 * (1.0 - x)
        deck = build_deck(
            case_label=f"C_phase_xfrac{x}",
            mole_fraction=x,
            rate_g=rate_g,
            rate_l=rate_l,
        )
        tag = f"{x:.2f}".replace('.', 'p')
        fname = out / f"caseC_phase_xfrac{tag}.in"
        fname.write_text(deck)
        print(f"  ✓ {fname.name}  (mole_frac={x}, rate_l={rate_l:.2e}, rate_g={rate_g:.2e})")

    # ===================================================================
    # Case D — BUOYANCY OVERRIDE (vary injection well z-position)
    # ===================================================================
    # Three injection positions:
    #   top    z = [0, 30]    — well in upper third
    #   middle z = [20, 80]   — baseline (already exists as Case A scco2_kappa1p0)
    #   bottom z = [70, 100]  — well in lower third
    print("\n=== Case D: BUOYANCY OVERRIDE (vary injection z-position) ===")
    case_D_positions = [
        ('top',    0.0,  30.0),
        ('bottom', 70.0, 100.0),
    ]
    for pos_name, z_low, z_high in case_D_positions:
        deck = build_deck(
            case_label=f"D_buoyancy_{pos_name}",
            mole_fraction=0.99,
            rate_g=5.0e-6,
            rate_l=0.0,
            well_z_low=z_low,
            well_z_high=z_high,
        )
        fname = out / f"caseD_buoyancy_{pos_name}.in"
        fname.write_text(deck)
        print(f"  ✓ {fname.name}  (well z = [{z_low}, {z_high}] m)")

    # ===================================================================
    # NOTE: A Case E (gas-residual-saturation sweep) was considered as a
    # test of the gas-water contact mechanism but was dropped. A uniform
    # change in residual saturation alters the trapped-gas QUANTITY, not
    # the gas-water contact MORPHOLOGY, so it does not isolate contact
    # area. Gas-water contact is instead addressed structurally in the
    # manuscript via the local-equilibrium dissolution argument (the
    # interfacial area does not enter the governing equations).
    # ===================================================================

    print(f"\nTotal decks generated: 6")
    print(f"  Case C (phase partitioning): 4")
    print(f"  Case D (buoyancy override):  2")
    print(f"\nOutput directory: {out.absolute()}")


if __name__ == "__main__":
    main()
